# Створення рядка цифр
digit_string = "9876543210"

# Перетворення рядка на множину
digit_set = set(digit_string)
print("Множина:", digit_set)

# Додавання ще однієї цифри до множини
digit_set.add("8")  # "8" вже є, але множина не допускає дублікатів

# Виведення оновленої множини
print("Оновлена множина:", digit_set)